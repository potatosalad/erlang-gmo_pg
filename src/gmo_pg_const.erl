-spec error_msg(binary()) -> binary().
error_msg(Code) ->
	error_msg_en(Code).

-spec error_msg(binary(), en | ja) -> binary().
error_msg(Code, en) ->
	error_msg_en(Code);
error_msg(Code, ja) ->
	error_msg_ja(Code).

-spec safe_error_msg(binary(), atom()) -> binary() | nil.
safe_error_msg(Code, Locale) ->
	try error_msg(Code, Locale) of
		Message when is_binary(Message) ->
			Message
	catch
		error:function_clause ->
			nil
	end.

